# n × (n - 1) × ... × 3 × 2 × 1 を n! と表す.
#
# 例えば, 10! = 10 × 9 × ... × 3 × 2 × 1 = 3628800 となる.
# この数の各桁の合計は 3 + 6 + 2 + 8 + 8 + 0 + 0 = 27 である.
#
# では, 100! の各桁の数字の和を求めよ.

def fact(n)
  (1..n).to_a.inject(1) {|f, i| f*i}
end

# 階上計算
num = fact(100)
# 数列を配列に
x = num.to_s.split('')
z = 0
# 数列を計算
x.each do |y|
  z += y.to_i
end

# Answer 648
p z
